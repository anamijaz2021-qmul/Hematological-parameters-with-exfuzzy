def decode_rule(rule_raw, feat_names):
    decoded = rule_raw
    for idx, name in enumerate(feat_names):
        # Only replace whole-word digit tokens, not inside decimals
        decoded = re.sub(rf'(?<![\d.])\b{idx}\b(?![\d.])', name, decoded)
    return decoded

